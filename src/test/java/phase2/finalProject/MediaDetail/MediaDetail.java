package phase2.finalProject.MediaDetail;

public class MediaDetail {
	
	    private String platform;
	    private String url;

	    public MediaDetail(String platform, String url) {
	        this.platform = platform;
	        this.url = url;
	    }

	    public String getPlatform() {
	        return platform;
	    }

	    public String getUrl() {
	        return url;
	    }
	


}
